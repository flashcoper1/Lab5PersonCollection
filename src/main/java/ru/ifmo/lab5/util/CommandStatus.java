package ru.ifmo.lab5.util;

/**
 * Перечисление статусов выполнения команды.
 * Используется в {@link CommandResult} для определения, была ли команда успешной.
 */
public enum CommandStatus {
    /**
     * Команда выполнена успешно.
     */
    SUCCESS,
    /**
     * Произошла ошибка при выполнении команды.
     */
    ERROR
}